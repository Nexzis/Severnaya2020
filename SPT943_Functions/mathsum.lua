--[[--------------------------------------------------------------------------------------------------------------
	@brief	- вычисление суммы значений массива
	@param	- t		table	массив
	@return	- summ	number	сумма значений массива
--------------------------------------------------------------------------------------------------------------]]--
function mathsum( t)
	local sum = 0;
		for k,v in pairs( t) do
			sum = sum + v;
		end
	return sum;
end